def weekend(market: str) -> Set[int]:
    return _WEEKEND.get(market, _DEFAULT_WEEKEND)


def holidays(market: str) -> Set[str]:
    """ISO holiday strings for a market. IN uses the live NSE list."""
    if market == "IN":
        try:
            from market_calendar import get_holidays

            return get_holidays()
        except Exception:
            return set()
    return _HOLIDAYS.get(market, set())


def is_trading_day(market: str, d: _dt.date, hol: Optional[Set[str]] = None) -> bool:
    if d.weekday() in weekend(market):
        return False
    h = hol if hol is not None else holidays(market)
    return d.isoformat() not in h


def trading_days(market: str, dates: Iterable[_dt.date]) -> List[_dt.date]:
    """Filter dates to this market's trading days (one holiday lookup)."""
    h = holidays(market)
    return [d for d in dates if is_trading_day(market, d, h)]


def next_trading_day(market: str, d: Optional[_dt.date] = None) -> _dt.date:
    d = (d or _dt.date.today()) + _dt.timedelta(days=1)
    h = holidays(market)
    while not is_trading_day(market, d, h):
        d += _dt.timedelta(days=1)
    return d


def last_trading_day(market: str, d: Optional[_dt.date] = None) -> _dt.date:
    d = d or _dt.date.today()
    h = holidays(market)
    while not is_trading_day(market, d, h):
        d -= _dt.timedelta(days=1)
    return d


def is_market_open_today(market: str) -> bool:
    return is_trading_day(market, _dt.date.today())


